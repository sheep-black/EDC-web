package com.example.edcspring.entity;

public class AOPData {
    private String AOPs;
    private String AOP_ID;
    private String name;
    private String pmid;

    public String getAOPs() {
        return AOPs;
    }

    public void setAOPs(String AOPs) {
        this.AOPs = AOPs;
    }

    public String getAOP_ID() {
        return AOP_ID;
    }

    public void setAOP_ID(String AOP_ID) {
        this.AOP_ID = AOP_ID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPmid() {
        return pmid;
    }

    public void setPmid(String pmid) {
        this.pmid = pmid;
    }
}
